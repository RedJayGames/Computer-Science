//init - (git init) used to initialize a repository and create a 
	//new hidden directory called .git
//clone - (git clone <repository URL>) creates a complete copy of the 
	//repository to your local system
//add - (git add <file>) defines the file as undergoing some sort of 
	//change in the next save
//commit - (git commit) confirms changes and asks you for explanation 
	//of changes (like creating comments for a changelog)
//diff - (gif diff) displays changes done to the newer file compared 
	//to the older file
//log - (git log) displays changelog in order from oldest changes to 
	//newest changes
